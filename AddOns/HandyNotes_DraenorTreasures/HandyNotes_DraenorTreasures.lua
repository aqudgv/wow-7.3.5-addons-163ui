local function GetItem(ID)
    if (ID == "824" or ID == "823" or ID == "829") then
        local currency, _, _ = GetCurrencyInfo(ID)

        if (currency ~= nil) then
            return currency
        else
            return "貨幣ID載入錯誤"
        end
    else
        local _, item, _, _, _, _, _, _, _, _ = GetItemInfo(ID)

        if (item ~= nil) then
            return item
        else
            return "物品ID載入錯誤"
        end
    end
end 

